package com.yjk.app.dto;

public class PositionDTO {

	private Double lon; //经度
	
	private Double lat; //纬度

	public Double getLon() {
		return lon;
	}

	public void setLon(Double lon) {
		this.lon = lon;
	}

	public Double getLat() {
		return lat;
	}

	public void setLat(Double lat) {
		this.lat = lat;
	}
	
	
}
